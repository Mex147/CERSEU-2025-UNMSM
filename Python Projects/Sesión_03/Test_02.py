"""Listas en Python"""

"""Listas -> insert(): inserta elementos en una posición dada y un valor"""

number = [40, 30.8, 100, 190, 580, 1500.45]

edad = 30

number.insert(1, edad)
print(f"Mi lista actualizada: {number}")

año = 1996
number.insert(4, año)
print(f"Mi lista actualizada: {number}")

number.insert(3,15)
