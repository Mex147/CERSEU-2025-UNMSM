"""Listas en Python"""

"""Listas: pop(): Quita un elemento de una posición dada"""

paises = ["Perú", "Canadá", "Argentina", "México", "España"]

print(f"Mi lista inicial es : {paises}")
paises.pop()
print(f"Mi lista actualizada es : {paises}")
print(f"paises[2] es : {paises[2]} ")
paises.pop(2)
print(f"Mi lista actualizada es : {paises}")

print(f"paises[2] es : {paises[2]} ")
paises.pop(2)
print(f"Mi lista actualizada es : {paises}")